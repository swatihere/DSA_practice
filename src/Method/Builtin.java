package Method;

public class Builtin {
    public static void main(String[] args) {
        System.out.println(Math.sqrt(100));  //square
        System.out.println(Math.cbrt(100));  //cube
        System.out.println(Math.floor(-7.8)); //greatest of integer
        System.out.println(Math.ceil(7.8));
        System.out.println(Math.min(3,9));   //minimum value
        System.out.println(Math.max(3,9));   //maximum value
        System.out.println(Math.pow(2,6));  //power

    }


}
